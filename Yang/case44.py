#两个 3 行 3 列的矩阵，实现其对应位置的数据相加，并返回一个新矩阵：
#方法一：
x=[[1,2,3],[1,2,3],[1,2,3]]
y=[[1,2,3],[1,2,3],[1,2,3]]
z=[]
for i in range(3):
    z.append([])
    for j in range(3):
        z[i].append(x[i][j]+y[i][j])
print (z)
